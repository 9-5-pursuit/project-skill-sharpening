/**
 * checkIfCharacterIsInString()
 * ---------------------
 * Checks if the given `character` appears in the `text`.
 * @param {string} text
 * @param {string} character
 * @returns {boolean}
 *
 * EXAMPLE:
 *  checkIfCharacterIsInString("hello there", "e");
 *  //> true
 *
 *  checkIfCharacterIsInString("hello there", "a");
 *  //> false
 */
function checkIfCharacterIsInString(text, character) {

    return text.indexOf(character) !== -1;

  }
  console.log(checkIfCharacterIsInString("hello there", "e")); // true
  
  console.log(checkIfCharacterIsInString("hello there", "a")); // false
  


/**
 * isEven()
 * ---------------------
 * Returns `true` if the number given is even. Otherwise, returns false.
 * @param {number} num
 * @returns {boolean}
 *
 * EXAMPLE:
 *  isEven(10);
 *  //> true
 *
 *  isEven(11);
 *  //> false
 */

function isEven(num) {
  return num % 2 === 0;
}

console.log(isEven(10));

console.log(isEven(11));


//if the num is divisible by 2, the function returns "true"


/**
 * isOdd()
 * ---------------------
 * Returns `true` if the number given is odd. Otherwise, returns `false`.
 * @param {number} num
 * @returns {boolean}
 *
 * EXAMPLE:
 *  isOdd(10);
 *  //> false
 *
 *  isOdd(11);
 *  //> true
 */
function isOdd(num) {
  return num % 2 !== 0;

}
console.log(isOdd(10));//10 is divisible by 2 so the function returns "false"

console.log(isOdd(11));//11 is not divisible by 2 so the function returns "true"
/**
 * isTruthy()
 * ---------------------
 * Returns `true` if the value given is a truthy value. Otherwise, returns `false`.
 * @param {*} val
 * @returns {boolean}
 *
 * EXAMPLE:
 *  isTruthy(10);
 *  //> true
 *
 *  isTruthy(null);
 *  //> false
 */
function isTruthy(val) {
    return !!val;
  }
  
  console.log(isTruthy(10)); // true
  console.log(isTruthy(null)); // false
  


/**
 * isFalsy()
 * ---------------------
 * Returns `true` if the value given is a falsy value. Otherwise, returns `false`.
 * @param {*} val
 * @returns {boolean}
 *
 * EXAMPLE:
 *  isFalsy(undefined);
 *  //> true
 *
 *  isFalsy(" ");
 *  //> false
 */
function isFalsy(val) {
  return !val;

}
console.log(isFalsy(undefined));//true

console.log(isFalsy(" ")); //false

